
a = ["Rojo", "Verde", "Celeste", "Violeta"]
b = ["Osorno", "Puerto Montt", "Puerto Varas", "Valdivia"]

#A
def mas():
    len(a).items[0]
    len(a).items[1]
    len(a).items[2]
    len(a).items[3]
    print(max(a))

#B
def menos():
    len(b).items[0]
    len(b).items[1]
    len(b).items[2]
    len(b).items[3]
    print(min(b))

#C
def concadenar():
    c = a + b

#D
def mayusculas():
    c.upper

#E
def ordenar():
    c.sorted

#Print
print(mas)
print(menos)
print(concadenar)
print(mayusculas)
print(ordenar)